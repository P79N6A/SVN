package com.jy.moudles.score.entityVO;

public class TargetVO {
	
	private int easyNum;
	private int secondaryNum;
	private int difficultNum;
	
	private String divideEasy;
	private String divideSecondary;
	private String divideDifficult;
	
	
	public int getEasyNum() {
		return easyNum;
	}
	public void setEasyNum(int easyNum) {
		this.easyNum = easyNum;
	}
	public int getSecondaryNum() {
		return secondaryNum;
	}
	public void setSecondaryNum(int secondaryNum) {
		this.secondaryNum = secondaryNum;
	}
	public int getDifficultNum() {
		return difficultNum;
	}
	public void setDifficultNum(int difficultNum) {
		this.difficultNum = difficultNum;
	}
	public String getDivideEasy() {
		return divideEasy;
	}
	public void setDivideEasy(String divideEasy) {
		this.divideEasy = divideEasy;
	}
	public String getDivideSecondary() {
		return divideSecondary;
	}
	public void setDivideSecondary(String divideSecondary) {
		this.divideSecondary = divideSecondary;
	}
	public String getDivideDifficult() {
		return divideDifficult;
	}
	public void setDivideDifficult(String divideDifficult) {
		this.divideDifficult = divideDifficult;
	}
	
	
	
}
